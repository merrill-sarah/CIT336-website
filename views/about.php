    <h1>About</h1>
    <p>Oh hello, my name is Sarah Merrill. If you share my unconditional love for 
        cats, often find yourself preoccupied by all things Kpop and Kdrama, or 
        just have an interest in reading about the mundane happenings in the life 
        of a stranger on the internet, then wow, this place may just be perfect for you.</p>
    <p>I am 21 years old and currently living at home taking online classes working 
        towards my web design and development degree. It's a great existence where 
        my little sister is my best friend and I only wash my hair once or twice 
        a week because I never leave the house anyways. This is the way I prefer 
        it though. Everything I need, my cat and computer, can be found in the comfort 
        of my room.</p>
    <p>Twig came into my life in January of 2014 when I adopted her off of Craigslist. 
        She definitely was the best Craigslist find ever and has worked her way deep 
        into my heart and into the heart of my family. Even the family cat grump, Iris, 
        doesn't mind being in the same room with Twig on occasions. The most important thing to 
        know about Twig is to never toucher her belly. She may lay on her back with it 
        wide open and seemingly just begging to rubbed, but don't fall for it. This a dangerous 
        trap. </p>
    <p>My obsession with Korean dramas started as it has for many addicts: one 
        fine day I stumbled across Boys Over Flowers on Netflix and before I 
        realized it, I was hooked. There was no going back. One thing led to another 
        and soon Kpop CDs began to pile up on shelves and posters lined my walls.  </p>
    
